let kelvin = 293; //constante

let celsius = kelvin - 293; //conversor

let fahrenheit = celsius * (9 / 5) + 32; //convierto a F
fahrenheit = Math.floor(fahrenheit);

console.log("The temperature es " + fahrenheit + " degrees Fahrenheit");

let newton = celsius * (33 / 100)

newton = Math.floor(newton)

console.log (`The temperature is ${newton} degrees Newton`)